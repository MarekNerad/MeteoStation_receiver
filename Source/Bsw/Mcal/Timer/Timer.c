void Timer_init(void)
{
   // Set the Timer Mode to CTC
   TCCR1A |= (1U << WGM11);
   // Set the value that you want to count to
   OCR1A = 0x7CU;
   //Set the ISR COMPA vector
   TIMSK |= (1U << OCIE1A);
   // set prescaler to 8 and start the timer
   TCCR1B |= (1U << CS01);
}
